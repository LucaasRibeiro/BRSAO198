def calculadora_media():
    print("\n=== CALCULADORA DE MÉDIA ESCOLAR ===")
    
    # Dados fornecidos
    nota1 = 7.5
    nota2 = 8.0
    nota3 = 6.5
    
    # Cálculo da média
    media = (nota1 + nota2 + nota3) / 3
    
    # Exibição dos resultados
    print(f"Nota 1: {nota1:.1f}")
    print(f"Nota 2: {nota2:.1f}")
    print(f"Nota 3: {nota3:.1f}")
    print(f"Média final: {media:.2f}")

# Executar a calculadora de média
calculadora_media()